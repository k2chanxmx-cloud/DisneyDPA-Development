-- DPA予測の自動学習ログ
create table if not exists public.prediction_logs (
    id bigint generated by default as identity primary key,
    target_date date not null,
    entry_time time not null,
    model_version text not null,
    predicted_at timestamptz not null default now(),

    crowd_score integer,
    ticket_price integer,
    official_open_time time,
    weather text,

    beauty_predicted_probability numeric,
    beauty_predicted_sellout_time time,
    baymax_predicted_probability numeric,
    baymax_predicted_sellout_time time,
    splash_predicted_probability numeric,
    splash_predicted_sellout_time time,

    beauty_actual_available boolean,
    beauty_actual_sellout_time time,
    baymax_actual_available boolean,
    baymax_actual_sellout_time time,
    splash_actual_available boolean,
    splash_actual_sellout_time time,

    evaluated_at timestamptz,
    prediction_payload jsonb,

    unique (target_date, entry_time, model_version)
);

create index if not exists prediction_logs_target_date_idx
    on public.prediction_logs (target_date desc);

create index if not exists prediction_logs_evaluated_at_idx
    on public.prediction_logs (evaluated_at);

alter table public.prediction_logs enable row level security;

